package ArrayListPractice;


public class TaskAnimal {

    String name;
    int age;
    String area;
    int dailyMeal;

    public TaskAnimal(){
    }

    public TaskAnimal(String name, int age, String area, int dailyMeal){
        this.name=name;
        this.age=age;
        this.area=area;
        this.dailyMeal=dailyMeal;
    }

    public void increaseMeal(){
            dailyMeal=dailyMeal+10;
        }






}
