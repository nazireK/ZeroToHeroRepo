package StaticPractice;

public class StaticReview {

    public static int add(int a, int b){
        int add=a+b;
        return add;
    }
    public static int subtract(int a, int b){
        int sub=a-b;
        return sub;
    }
    public static int divide(int a, int b){
        int divide=a/b;
        return divide;
    }
    public static int multiple(int a, int b){
        int multiply=a*b;
        return multiply;
    }


}
